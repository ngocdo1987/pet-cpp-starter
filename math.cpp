#include <iostream>
#include <cmath>
using namespace std;

// Math examples
int main() {
    cout << "Max is: " << max(5, 10) << endl;
    cout << "Min is: " << min(5, 10) << endl;
    cout << "Sqrt 64 is: " << sqrt(64) << endl;
    cout << "Round 2.6 is: " << round(2.6) << endl;
    cout << "Log 2 is: " << log(2) << endl;
}